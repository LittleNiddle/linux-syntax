# vi 편집기로 파일 열기
vi 파일명

# vi 편집기종료
:wq : 저장 후 종료
:q  : 저장하지 않고 종료

# 입력모드로 전환
a  : 다음 커서부터 입력
i  : 현재 커서에서 입력
o  : 다음 줄부터 입력

# 명령모드로 전환 : esc
x  :  한 칸 삭제
dd : 한 줄 삭제
yy : 한 줄 복사
p  : 다음 줄에 붙여넣기
G  : 맨 끝으로 이동
gg : 맨 앞으로 이동
Ctrl + f : 한 페이지 아래로 이동
Ctrl + b : 한 페이지 위로 이동

# 검색 : /검색하고자하는문자열 -> 엔터 -> n 다음 결과로 이동
